	AREA IM, CODE, READONLY
	ENTRY

MAIN
	; LOAD ADDRESS OF VARIABLES
	LDR R5, =A
	LDR R6, =B
	LDR R7, =Y

	# SET A TO 220 AND STORE VALUES
	MOV R0, #220
	STR R0, [R5]

	# SET B TO 55 AND STORE VALUES
	MOV R1, #55
	STR R1, [R6]

	# CALL GCD SUBROUTINE
	BL GCD

FIN
	

HERE B HERE

; SUBROUTINE TO COMPUTE THE GREATEST COMMON DIVISOR
; INPUT IS EXPECTED ON R0 AND R1
GCD
	CMP R0, R1
	BEQ FIN
	BLT LESS
	SUBS R0, R0, R1
	B GCD

LESS
	SUBS R1, R1, R0
	B GCD

